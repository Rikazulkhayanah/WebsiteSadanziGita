    <h2>Jenis makanan yang harus dihindari sebelum anak berusia 1 tahun</h2>
    <ul><li>Hindari menambahkan garam dan gula</li></ul>
    <ul><li>Hindari yang menyebabkan tersendak</li></ul>
    <ul><li>Susu sapi segar & produk olahannya</li></ul>
    <ul><li>makanan mengandung merkuri tinggi </li></ul>
    <ul><li>Madu mengandung spora bakteri</li></ul>
    <h2>Pemberian makanan aktif/responsif</h2>
    <ol>
        <li>Bersabar & dorong terus bayi makan lebih banyak</li>
        <li>jika anak menolak, pangkulah anak sewaktu diberi makan/menghadap ke  anak kalau anak dipangku orang lain</li>
        <li>Tawarkan makanan secara berkali-kali</li>
        <li>Berinteraksilah dengan anak dan kurangi gangguan watu diberi makan</li>
        <li>jangna paksa anak untuk makan</li>
    </ol>
    <h2>Pesan penting saat Memberikan makanan</h2>
    <ol>
        <li>Beri makanan bayi dengan tangan, peralatan & cangkir yang bersih</li>
        <li>gunakan sendok 7 cangkir yang bersih untuk memberikan makanan dan cairan kepada bayi</li>
        <li>Jangan gunakan botol,kempeng/dot karena peralatan tersebut sulit dibersihkan</li>
        <li>Simpan makanan yang digunakan akan diberikan kepada bayi di tempat yang bersih dan aman</li>
    </ol>
    <h2>Waktu penting untuk mencuci tangan</h2>
    <ul>
        <li>Sebelum menyiapkan makanan dan memberi makan bayi</li>
        <li>Sebelum makan</li>
        <li>Setelah ketoilet</li>
        <li>Setelah membersihkan kotoran bayi</li>
        <li>Setelah anak bermain</li>
    </ul>
    <h2>7 langkah higiene tangan</h2>
    <ul>
        <li>Telapak dengan telapak</li>
        <li>Telapak kanan diatas punggu tangan kiri dan telapak kiri diatas punggung tangan kanan</li>
        <li>telapak dengan telapak & jari saling terkait</li>
        <li>letakkan punggung jari pada telapak satunya dengan jari saling mengunci</li>
        <li>Jempol kanan digosok memutar oleh telapak kiri & sebaliknya</li>
        <li>jari kiri menguncup, gosok memutar ke kanan & ke kiri pada telapak kanan & sebaliknya</li>
        <li>Pegang pergelangan tangan kiri dengan tangan kanan & sebaliknya gerakan</li>
    </ul>
    
